#!/usr/bin/env python3

def add(a: float, b: float) -> float:
    """
    Add two float numbers and return their sum as a float.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b

if __name__ == "__main__":
    add(1.11, 2.22)
